Queries aninhadas, também conhecidas como subconsultas, 
são consultas SQL que são inseridas dentro de outras consultas. 

 Elas permitem que você execute uma consulta dentro de outra consulta, 
 permitindo operações mais complexas e precisas. As subconsultas podem 
 ser usadas em várias partes de uma consulta maior, como na cláusula WHERE, 
 cláusula FROM ou até mesmo na cláusula SELECT.